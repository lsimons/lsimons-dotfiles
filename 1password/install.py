#!/usr/bin/env python3
"""Installation script for 1Password CLI"""

import os
import re
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent / 'script'))
from helpers import (
    DOTFILES_ROOT,
    brew_install,
    command_exists,
    dry,
    error,
    get_machine_config,
    info,
    is_dry_run,
    op_secret,
    parse_dry_run,
    run_cmd,
    success,
    write_file,
)


_VALID_VAR_NAME = re.compile(r'^[A-Za-z_][A-Za-z0-9_]*$')
_VALID_ACCOUNT_NAME = re.compile(r'^[A-Za-z0-9_.-]+$')


def _parse_base_env_file(path):
    """Read 1password/.env.1password into an ordered list of entries.

    The base file is shared across machines, so it can only carry
    secrets that live in the default 1Password account. Each line is
    ``VAR="op://..."`` (the existing format). Lines that don't match
    are skipped silently.

    Returns a list of (var_name, secret_value) where secret_value is
    a bare ref string — :func:`op_secret` will assign it the default
    account.
    """
    entries = []
    if not path.exists():
        return entries
    for line in path.read_text().splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith('#'):
            continue
        if '=' not in stripped:
            continue
        var, _, raw = stripped.partition('=')
        var = var.strip()
        raw = raw.strip()
        # Strip optional surrounding quotes
        if len(raw) >= 2 and raw[0] in ('"', "'") and raw[-1] == raw[0]:
            raw = raw[1:-1]
        if not raw.startswith('op://'):
            continue
        entries.append((var, raw))
    return entries


def generate_secrets_loader():
    """Generate ~/.config/1password/secrets-loader.sh.

    The loader is a list of ``_op_load VAR ACCOUNT REF`` calls, one per
    secret. ``1password/1password.sh`` defines ``_op_load`` (which runs
    ``op read --account ACCOUNT REF``) and sources this file when the
    cache is stale, then writes the resolved values to the cache.

    Resolving each secret with its own ``op read`` call (rather than
    one shared ``op run --env-file=``) is what lets us mix accounts
    in a single machine.
    """
    home = Path.home()
    xdg_config_home = Path(os.environ.get('XDG_CONFIG_HOME', home / '.config'))
    config_dir = xdg_config_home / '1password'
    loader_file = config_dir / 'secrets-loader.sh'

    base_env = DOTFILES_ROOT / '1password' / '.env.1password'

    # Build var -> (ref, account), preserving "machine overrides base"
    resolved = {}
    for var, value in _parse_base_env_file(base_env):
        resolved[var] = op_secret(value)

    machine_config, hostname = get_machine_config()
    secrets = machine_config.get('1password', {}).get('secrets', {})
    for var, value in secrets.items():
        resolved[var] = op_secret(value)

    lines = [
        '# Generated by 1password/install.py — do not edit by hand',
        '# Sourced by 1password/1password.sh; relies on _op_load.',
        '',
    ]
    for var in sorted(resolved):
        ref, account = resolved[var]
        if not _VALID_VAR_NAME.match(var):
            error(f"Invalid 1Password env var name: {var!r}")
            continue
        if not _VALID_ACCOUNT_NAME.match(account):
            error(
                f"Invalid 1Password account for {var}: {account!r} "
                f"(allowed: alnum, '_', '-', '.')"
            )
            continue
        # account / var are validated above; ref goes through shell
        # quoting to handle spaces and special chars.
        ref_q = "'" + ref.replace("'", "'\\''") + "'"
        lines.append(f'_op_load {var} {account} {ref_q}')
    content = '\n'.join(lines) + '\n'

    # Migrate away from the old env-file path — it's no longer
    # consumed and would otherwise sit there confusingly.
    legacy_env_file = config_dir / 'env'
    if legacy_env_file.exists():
        if is_dry_run():
            dry(f"would remove legacy {legacy_env_file}")
        else:
            legacy_env_file.unlink()
            info(f"Removed legacy {legacy_env_file}")

    if loader_file.exists() and loader_file.read_text() == content:
        success("1Password secrets-loader already up to date")
    else:
        write_file(loader_file, content, mode=0o600)
        success(f"Generated 1Password secrets-loader for {hostname}")

    # Force the next shell to re-resolve so it sees account/ref edits.
    xdg_cache_home = Path(os.environ.get('XDG_CACHE_HOME', home / '.cache'))
    cache_file = xdg_cache_home / '1password' / 'secrets.sh'
    if cache_file.exists():
        if is_dry_run():
            dry(f"would delete stale cache {cache_file}")
        else:
            cache_file.unlink()
            info("Deleted stale secrets cache")

    return str(cache_file)


def install_launch_agent(cache_file_path):
    """Install LaunchAgent for cache cleanup on login/reboot."""
    home = Path.home()
    plist_name = 'com.lsimons.1password-cache-cleanup.plist'
    template = DOTFILES_ROOT / '1password' / plist_name
    agents_dir = home / 'Library' / 'LaunchAgents'
    dest = agents_dir / plist_name

    if not template.exists():
        error(f"LaunchAgent template not found: {template}")
        return False

    content = template.read_text().replace('__CACHE_FILE__', cache_file_path)

    if is_dry_run():
        dry(f"would install LaunchAgent {dest}")
        return True

    agents_dir.mkdir(parents=True, exist_ok=True)

    # Unload old agent if present
    if dest.exists():
        run_cmd(
            ['launchctl', 'unload', str(dest)],
            capture_output=True,
            check=False,
        )

    # Check if content changed
    if dest.exists() and dest.read_text() == content:
        # Reload in case it was unloaded
        run_cmd(
            ['launchctl', 'load', str(dest)],
            capture_output=True,
            check=False,
        )
        success("LaunchAgent already up to date")
        return True

    write_file(dest, content)
    run_cmd(
        ['launchctl', 'load', str(dest)],
        capture_output=True,
        check=False,
    )
    success("Installed 1Password cache cleanup LaunchAgent")
    return True


def main():
    parse_dry_run()
    info("Installing 1Password CLI...")

    if command_exists('op'):
        success("1Password CLI already installed")
    elif brew_install('1password-cli', cask=True):
        success("1Password CLI installed")
    else:
        error("Failed to install 1Password CLI")
        return 1

    cache_file_path = generate_secrets_loader()
    install_launch_agent(cache_file_path)
    return 0


if __name__ == '__main__':
    sys.exit(main())
